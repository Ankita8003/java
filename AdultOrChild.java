
 public class AdultOrChild {
    public static void main(String[] args) {
        // Assign age
        int age = 20;
         //using if statement to check if adult or child
         if(age >= 18) 
         {
            System.out.println("The person is an adult.");
         }
         if(age < 18)
         {
            System.out.println("The person is a child.");
         }
    }
}
    

